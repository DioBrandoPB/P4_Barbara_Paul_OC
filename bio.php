            <p>Romancier français né en 1974 à Antibes, Jean Forteroche est l'auteur le plus vendu en France. Écrivain prolifique, il sort chaque année un nouveau roman qui atteint directement le top des ventes. Traduit en 40 langues, il est également l'un des auteurs français les plus lus à l'étranger.</p>

            Un professeur devenu auteur par accident
                Jean Forteroche naît le 6 juin 1974 à Antibes. Fils d'une directrice de bibliothèque, il tombe amoureux de la littérature à l'âge de 10 ans en découvrant Les Hauts de Hurlevent d'Emily Brontë. Il passe alors tout son temps libre à dévorer les classiques des auteurs français, anglo-saxons et russes. Son amour pour la lecture fait petit à petit place à un désir d'écriture. C'est en remportant un concours de nouvelles organisé par un professeur de seconde que son envie de raconter des histoires se confirme.

                À 19 ans, Jean Forteroche part travailler 4 mois comme vendeur de crèmes glacées à New York. Ce voyage le marque énormément et inspire le décor de ses 5 premiers romans. De retour en France, il suit les pas de son père financier en se lançant dans une licence en sciences économiques à l'université de Nice puis dans un CAPES en sciences économiques et sociales à Montpellier. Il ne quitte pratiquement pas les bancs de l'école puisqu'il devient rapidement professeur de cette matière dans un lycée de Phalsbourg et travaille comme formateur à l'IUFM de Lorraine de 1999 à 2003.</p>

                <p>L'an 2000 est un véritable tournant pour l'auteur. Après avoir sorti un premier roman, Skidamarink, Jean Forteroche survit miraculeusement à un grave accident de voiture. Cet événement marquant lui fait soudainement prendre conscience que la mort peut arriver à tout moment. Ressentant un vif besoin d'écrire sur l'urgence de vivre, il commence à se documenter sur les cas d'expérience de mort imminente. C'est ainsi qu'il donne naissance en 2004 à son premier grand succès, Et après.</p>

                <p>Ce n'est que le début d'une suite de triomphes. Jean Forteroche accumule les best-sellers en sortant chaque année un ouvrage que les lecteurs s'arrachent. Malgré sa popularité croissante, Jean Forteroche poursuit toutefois son travail de professeur au Centre international de Varbonne jusqu'en 2008. S'il ne vit aujourd'hui plus que de l'écriture, il garde encore les pieds sur terre et reste très discret sur sa vie privée.</p>
                Entre polar, romance et fantastique
                Jonglant avec les genres, Jean Forteroche est un auteur qui se distingue pour son sens du suspense et son style très cinématographique. Si ses romans se rapprochent du thriller, ils font également la part belle à l'amour et aux sentiments. Grand fan de Stephen King depuis son adolescence, Jean Forteroche aime également glisser des éléments surnaturels dans ses ouvrages, comme dans Sauve-Moi, Parce que je t'aime ou Je reviens te chercher. Au fil des années, il semble toutefois mettre de côté le fantastique pour se rapprocher davantage du polar.</À>

                <p>L'un des auteurs les plus lus de France
                Avec plus de 28 millions d'exemplaires vendus depuis le début de sa carrière, Jean Forteroche est chaque année élu le romancier français le plus lu depuis 2011. Ses romans traduits en 40 langues font également fureur à l'étranger, particulièrement en Corée où Sauve-Moi a été numéro 1 des ventes pendant plusieurs semaines. Et après a quant à lui remporté le prix du meilleur roman adaptable au cinéma en 2004 et le prix italien Scrivere per Amore en 2005. En 2012, Jean Forteroche est nommé Chevalier de l'ordre des Arts et des Lettres. S'il ne fait pas l'unanimité chez les critiques, il est sans conteste l'écrivain préféré des Français. Une popularité qui se confirme d'année en année.</p>

